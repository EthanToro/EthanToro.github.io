				<h1>SCHEDULED MAINTENANCE</h1>
				<hr class="rule_purple" />
				<p>Companies today must rely upon their compressed air system to provide them with reliable uptime in order to realize maximum return on their investment. Unscheduled downtime can never be fully incorporated into production plans.</p>
				<p>In addition, lost production hours, high scrap rates, extraordinary maintenance and unproductive operator hours must be counted as costs against manufacturing. The detrimental effect on company profitability from this situation is usually random and uncontrolled.</p>
				<p>Corey Compressor, Inc. has taken steps to help by offering a comprehensive <strong>Preventive Maintenance Program</strong>. This plan will establish base line performance criteria for each machine that it is applied to. Each subsequent check of the machine will then be compared to this information in order to predict and address failures through <strong>scheduled maintenance</strong> rather than reacting to <a href="services_Emergency_Service.php"><u>emergency</u></a> situations.</p>
				<p>Another benefit of this program is the statistical information that it produces. Detailed trend analysis can be reviewed and easily applied in a real world manner. Replacement parts that may require long lead times can now be ordered in advance to meet critical needs or even stocked on-site. With this information, decisions regarding inventory investment and control will be based on historical fact rather than a "best guess".</p>
				<p>The primary goal of scheduled preventive maintenance is to help to create a dependable product flow from your manufacturing facility by ensuring dependable equipment.<p>
