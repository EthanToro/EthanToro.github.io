				<h1>PREVENTIVE MAINTENANCE PROGRAM</h1>
				<hr class="rule_blue" />
				<p>The Corey Compressor, Inc. Service Technician will perform an overall visual inspection of the machine condition. In order to ensure a comprehensive program we will also include the following routine checks, electrical checks and alignments:
				<ul>
						<li>Change oil as needed (synthetic or mineral based oil as required)</li>
						<li>Inspect belt tension make any belt adjustment</li>
						<li>Noise inspection and evaluation</li>
						<li>Inspect electrical connections</li>
						<li>Take amp, temperature and pressure readings</li>
						<li>Replacement of air filter</li>
						<li>Replacement of oil filter</li>
						<li>Replace Separator</li>
						<li>Inspect / clean drain valves</li>
						<li>Perform safety checks</li>
						<li>General cleaning of coolers</li>
						<li>Inspect auxiliary equipment for proper operation</li>
				</ul>
				</p>
